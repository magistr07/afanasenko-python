voyage = {"Мексика", "Канада", "Израиль", "Италия"}
reina_tur = {"Англия", "Япония", "Канада", "ЮАР"}

diff_voyage = voyage - reina_tur
print("1. Туры Вояж, отсутствующие в РейнаТур:", diff_voyage)

diff_reina = reina_tur - voyage
print("2. Туры РейнаТур, отсутствующие в Вояж:", diff_reina)

intersection = voyage & reina_tur
print("3. Одинаковые туры:", intersection)

are_equal = voyage == reina_tur
print("4. Перечни туров равны?", are_equal)
